/// This extension allows developers to access a [PlatformIcon] object,
/// containing both Material and Cupertino versions of the icon,
/// using the convenient [data] getter:
///
/// ```dart
/// final icon = AppIconData.settings.data;
/// final materialIcon = icon.material;
/// final cupertinoIcon = icon.cupertino;
/// ```
///
/// The mapping ensures consistent iconography across platforms by providing:
/// - Native look & feel for both iOS and Android
/// - A single source of truth for icon references in the app
